package Dhanu;
import java.util.Scanner;
public class DP3 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int var1 = sc.nextInt();
		System.out.println("The integer value is:" +var1);
		System.out.println("---------");
		float var2 = sc.nextFloat();
		System.out.println("The float value is:" +var2);
		System.out.println("---------");
		double var3 = sc.nextDouble();
		System.out.println("The double value is:" +var3);
		System.out.println("---------");
		byte var4 = sc.nextByte();
		System.out.println("The byte value is:" +var4);
		System.out.println("---------");
		short var5 = sc.nextShort();
		System.out.println("The short value is:" +var5);
		System.out.println("---------");
		long var6 = sc.nextLong();
		System.out.println("The long value is:" +var6);
		System.out.println("---------");
		String s1 = sc.next();
		System.out.println("The string value is:" +s1);
		System.out.println("---------");
		String s2 = sc.nextLine();
		System.out.println("The string value is:" +s2);
		System.out.println("---------");

	}

}
